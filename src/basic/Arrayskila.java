package basic;

public class Arrayskila {

	public static void main(String[] args) {
		
		double [] num = new double [15];
		
		num[0] = 213.654;
		System.out.println(num[0]);
		num[14] = 32.564;
		System.out.println(num[14]);
		
		String [] abad = new String[20];
		abad[0] = "Ahmadi";
		System.out.println(abad[0]);
		
		int [] ber = new int [100];
		ber[59] = 2324;
		System.out.println(ber[59]);
	}

}
